<div class="text"><h3>What is IELTS Score Band?</h3>
	<p>
All IELTS scores are between 0 and 9. You can also get .5 scores as well (for example, 6.5 or 7.5). <br/>You will get a band score for each skill (listening, reading, writing and speaking) and also an overview band score. <br/> The overall band score is the average score of all the skills.
</p>
