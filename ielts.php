	<h3>ACADEMIC IELTS: </h3>
	<p>The IELTS Academic test is for people applying for higher education or professional registration in an English speaking environment. <br/>It reflects some of the features of academic language and assesses whether you are ready to begin studying or training. <br/>

Unlike the general training version, the academic version allows test takers to demonstrate some reading and writing skills that aren’t necessarily part of the everyday communicative experience of English speakers.<br/> These are considered to be distinct from reading and writing skills for general purposes
</p>
<h3>GENERAL IELTS:</h3>
<p>The IELTS General Training test is for those who are going to English speaking countries for secondary education, work experience or training programs. <br/> It is also a requirement for migration to Australia, Canada, New Zealand and the UK.  <br/>The test focuses on basic survival skills in broad social and workplace contexts.</p>
	
